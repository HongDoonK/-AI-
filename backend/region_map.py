def get_region_code(sido: str, sigungu: str) -> str | None:
    """
    시도명 + 자치구명으로 법정동 5자리 코드 반환
    없으면 None 반환
    예: get_region_code("서울", "강남구") → "11680"
    """
    return REGION_CODE_MAP.get(sido, {}).get(sigungu)


def get_sido_list() -> list:
    """시도 목록 반환 (Streamlit 드롭다운용)"""
    return list(REGION_CODE_MAP.keys())


def get_sigungu_list(sido: str) -> list:
    """해당 시도의 자치구 목록 반환 (Streamlit 드롭다운용)"""
    return list(REGION_CODE_MAP.get(sido, {}).keys())